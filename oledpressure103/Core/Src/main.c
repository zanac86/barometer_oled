void init_my_devices()
{
	HAL_Delay(500);
	OLED_Init(&hi2c1);
	OLED_FontSet(Font_MSX_6x8_eng);
	OLED_SetContrast(2);

	OLED_Clear(0);

	OLED_DrawStr("OLED Ok", 0, 0, 1);
	OLED_UpdateScreen();
	HAL_Delay(200);

	bmp280_init_default_params(&bmp280.params);
	bmp280.addr = BMP280_I2C_ADDRESS_0;
	bmp280.i2c = &hi2c2;

	if (!bmp280_init(&bmp280, &bmp280.params))
	{
		OLED_DrawStr("NO BMP", 64, 0, 1);
		OLED_UpdateScreen();
		HAL_Delay(1000);
	}
	else
	{
		if (bmp280.id == BME280_CHIP_ID)
		{
			OLED_DrawStr("BME 280", 64, 0, 1);
			OLED_UpdateScreen();
		}
		else
		{
			OLED_DrawStr("BMP 280", 64, 0, 1);
			OLED_UpdateScreen();
		}
	}

	char str[20];
	sprintf(str, "S=%ld", HAL_RCC_GetSysClockFreq());
	OLED_DrawStr(str, 0, 16, 1);
	sprintf(str, "H=%ld", HAL_RCC_GetHCLKFreq());
	OLED_DrawStr(str, 64, 16, 1);
	sprintf(str, "1=%ld", HAL_RCC_GetPCLK1Freq());
	OLED_DrawStr(str, 0, 24, 1);
	sprintf(str, "2=%ld", HAL_RCC_GetPCLK2Freq());
	OLED_DrawStr(str, 64, 24, 1);
	OLED_UpdateScreen();

	HAL_Delay(3000);

	init_history();
}

void read_measure()
{
	int32_t t;
	uint32_t p, h;
	bmp280_read_fixed(&bmp280, &t, &p, &h);
	pressure = (int32_t) (p >> 8);

	if (pressure < MIN_PRESSURE) // 700mmHg
	{
		pressure = MIN_PRESSURE;
	}
	if (pressure > MAX_PRESSURE) // 800mmHg
	{
		pressure = MAX_PRESSURE;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Instance == TIM3) //check if the interrupt comes from TIM1
	{
		new_measure_do = 1;
	}
}

void process_first_time_measure()
{
	for (uint16_t i = 0; i < 3; i++)
	{
		HAL_Delay(200);
		read_measure();
	}
	append_measure_to_history(pressure);
	set_now_pressure(pressure);
	display_mode = 0;
	display(display_mode);

}

void process_timer_event()
{
//	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
	seconds = (seconds + 1) % SECONDS_PER_HOUR;
	if ((seconds % SECONDS_READ_PRESSURE) == 0)
	{
		read_measure();
		set_now_pressure(pressure);
	}
	if ((seconds % SECONDS_CHANGE_DISPLAY) == 0)
	{
		display_mode = (display_mode + 1) % DISPLAY_MODES_COUNT;
		display(display_mode);
	}
	if ((seconds % SECONDS_UPDATE_HISTORY) == 0)
	{
		append_measure_to_history(pressure);
	}

//	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
}

